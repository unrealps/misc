package main

import "fmt"

func main() {
	// Variable declaration with type inference and the := operator, without using var keyword
	name := "Alice"
	age := 30
	height := 5.7
	isStudent := false

	// Print the variables
	fmt.Println("Name:", name)
	fmt.Println("Age:", age)
	fmt.Println("Height:", height)
	fmt.Println("Is Student:", isStudent)
}
